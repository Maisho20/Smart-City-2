$(document).ready(function() {
    $('.datepicker').datepicker({
        format: 'yyyy-mm-dd', // Format tanggal yang diinginkan (YYYY-MM-DD)
        autoclose: true, // Menutup kalender setelah memilih tanggal
        orientation: 'bottom' // Posisi tampilan kalender (bawah)
    });
});

